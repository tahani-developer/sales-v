package com.dr7.salesmanmanager.Modles;

public class Password {
    private  int password_type;
    private  String password_no;

    public Password() {
    }

    public int getPassword_type() {
        return password_type;
    }

    public void setPassword_type(int password_type) {
        this.password_type = password_type;
    }

    public String getPassword_no() {
        return password_no;
    }

    public void setPassword_no(String password_no) {
        this.password_no = password_no;
    }
}
